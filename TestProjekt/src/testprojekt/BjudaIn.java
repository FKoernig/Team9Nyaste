    public void showMeetings() {
        try {
            String query = "select mid, amne from mote where anvandar_ID = " + uid;
            ArrayList<HashMap<String, String>> meetings = idb.fetchRows(query);
            for (HashMap<String, String> meeting : meetings) {
                String display = meeting.get("MID") + ": " + meeting.get("AMNE");
                cbMeetings.addItem(display);
            }
        } catch (InfException ie) {
            System.out.println(ie.getMessage());
        }
    }
    
    public String findUserID(String username) {    
        String result = "";
        try {
            String query = "SELECT Anvandar_ID FROM Anvandare WHERE Anvandar_namn = '" + username + "'";
            result = idb.fetchSingle(query);
        } catch(InfException ie) {
                System.out.println(ie.getMessage());
        }
        return result;
    }
    
