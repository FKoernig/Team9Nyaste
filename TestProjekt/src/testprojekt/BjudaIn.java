        String attbjuda = txtattbjuda.getText();
        String string = cbMeetings.getSelectedItem().toString();
        String meetingID = string.substring(0, string.indexOf(":"));

        try {
            if (validator.validateInvite(meetingID, attbjuda, uid)) {
                System.out.println("INSERT INTO inbjudningar VALUES (" + findUserID(attbjuda) + ", " + meetingID + ")");
                idb.insert("INSERT INTO inbjudningar VALUES (" + findUserID(attbjuda) + ", " + meetingID + ")");
                JOptionPane.showMessageDialog(null, "Inbjudan skickad");
            }
        } catch(InfException e) {
            System.out.println(e.getMessage());
        }   
